package qxsl.draft;

/**
 * 運用者の個人名を表す属性の実装です。
 *
 *
 * @author 無線部開発班
 *
 * @since 2013/06/08
 */
public final class Name extends Qxsl<String> {
	/**
	 * 運用者名を指定して属性を構築します。
	 *
	 *
	 * @param name 運用者名
	 */
	public Name(String name) {
		super(NAME, name);
	}

	/**
	 * この属性の値が有効か検証します。
	 *
	 *
	 * @return 有効な場合は真
	 *
	 * @since 2022/08/01
	 */
	@Override
	public final boolean valid() {
		return true;
	}
}
